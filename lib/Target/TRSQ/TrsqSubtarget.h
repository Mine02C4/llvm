#ifndef LLVM_LIB_TARGET_TRSQ_TRSQSUBTARGET_H
#define LLVM_LIB_TARGET_TRSQ_TRSQSUBTARGET_H

#include "TrsqFrameLowering.h"
#include "TrsqISelLowering.h"
#include "TrsqInstrInfo.h"
#include "TrsqRegisterInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "TrsqGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class TrsqSubtarget : public TrsqGenSubtargetInfo {
  virtual void anchor();
  bool ExtendedInsts;
  TrsqFrameLowering FrameLowering;
  TrsqInstrInfo InstrInfo;
  TrsqTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  TrsqSubtarget(const Triple &TT, const std::string &CPU,
                  const std::string &FS, const TargetMachine &TM);

  TrsqSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const TrsqInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
  const TrsqTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};
} // End llvm namespace

#endif  // LLVM_TARGET_TRSQ_SUBTARGET_H
